<?php

class Controller {


	/*************************************************************************
	 ATTRIBUTES
	 *************************************************************************/
	public $view;


	/*************************************************************************
	  CONSTRUCTOR                   
	 *************************************************************************/
	public function __construct( ) {
		$this->view = new View( );
	}


	/*************************************************************************
	  PRIVATE METHODS                   
	 *************************************************************************/
	protected function render( $template, $var ) {
		return $this->view->render( $template, $var );
	}
}
